// ---- algorithm diagnostic messages (episode reasons + warnings) -------------
// Called by algorithms with the run language. Numbers are formatted locale-aware.
export function am(lang = DEFAULT_LANG) {
  const L = LANGS.some((x) => x.code === lang) ? lang : DEFAULT_LANG;
  const D = (sec) => fmtDur(L, sec);
  const A = ALGO[L] || ALGO.fr;
  return {
    // multichannel velocity / rain fragments
    velNA: () => A.velNA,
    velStable: (p) => interpolate(A.velStable, { v: signed(L, p, 1) }),
    velVal: (p) => interpolate(A.velVal, { v: signed(L, p, 1) }),
    rainPresent: () => A.rainPresent,
    rainAbsent: () => A.rainAbsent,
    rainNA: () => A.rainNA,
    driftReason: (depthSigma, durSec, velPart, rainPart) =>
      interpolate(A.driftReason, { s: signed(L, depthSigma, 1), d: D(durSec), vel: velPart, rain: rainPart }),
    restriction: (dStep, vStepPct, durSec) =>
      interpolate(A.restriction, { d: signed(L, dStep, 1), v: signed(L, vStepPct, 1), dur: D(durSec) }),
    excursion: (durSec, driftReasonStr) =>
      interpolate(A.excursion, { dur: D(durSec), reason: driftReasonStr }),
    rainEvent: (durSec) => interpolate(A.rainEvent, { dur: D(durSec) }),
    hydraulicEvent: (durSec) => interpolate(A.hydraulicEvent, { dur: D(durSec) }),
    fault: (nPts) => interpolate(A.fault, { n: nf(L, nPts) }),
    warnVelAbsent: () => A.warnVelAbsent,
    warnRainNA: () => A.warnRainNA,
    warnNoTimestamp: () => A.warnNoTimestamp,
    // canari
    canariDrift: (nStarts, nClean) => interpolate(A.canariDrift, { n: nf(L, nStarts), c: nf(L, nClean) }),
    canariNoDrift: (nClean) => interpolate(A.canariNoDrift, { c: nf(L, nClean) }),
    // cusum
    tooShortDrift: () => A.tooShortDrift,
    cusumDrift: (n, ref) => interpolate(A.cusumDrift, { n: nf(L, n), ref: nf(L, ref, 2) }),
    cusumNoDrift: () => A.cusumNoDrift,
    // forecast / gbdt
    naiveForecast: () => A.naiveForecast,
    cleanedForecast: (n) => interpolate(A.cleanedForecast, { n: nf(L, n) }),
    tooShortGbdt: () => A.tooShortGbdt,
    cleanedTrain: (n) => interpolate(A.cleanedTrain, { n: nf(L, n) }),
    // skf
    tooShortSkf: () => A.tooShortSkf,
    qualityInterp: (n) => interpolate(A.qualityInterp, { n: nf(L, n) }),
    transitionReason: (prMax01, durSec, trendPerDay) =>
      interpolate(A.transitionReason, { pr: pct(L, prMax01 * 100), dur: D(durSec), tr: signed(L, trendPerDay, 1) }),
  };
}

