const UNIT_WORD = {
  fr: { mm: "millimètres", m: "mètres", ft: "pieds" },
  en: { mm: "millimetres", m: "metres", ft: "feet" },
  it: { mm: "millimetri", m: "metri", ft: "piedi" },
  es: { mm: "milímetros", m: "metros", ft: "pies" },
};
export function unitLabel(units, lang = getLang()) {
  if (!units) return "";
  const base = (UNIT_WORD[lang] || UNIT_WORD.fr)[units.depth] || units.depth;
  return units.autoDetected ? t("unit.auto", { u: base }, lang) : base;
}

// ---- model metadata overlays (fr = registry, others here) -------------------
// localizeModel returns a model view with translated label/description/tips and
// per-param label/help/options; anything missing falls back to the registry.
export function localizeModel(model, lang = getLang()) {
  if (lang === "fr" || !MODEL_I18N[lang] || !MODEL_I18N[lang][model.id]) return model;
  const tr = MODEL_I18N[lang][model.id];
  return {
    ...model,
    label: tr.label ?? model.label,
    description: tr.description ?? model.description,
    tips: tr.tips ?? model.tips,
    params: model.params.map((p) => {
      const pt = tr.params && tr.params[p.key];
      if (!pt) return p;
      const out = { ...p, label: pt.label ?? p.label, help: pt.help ?? p.help };
      if (p.options && pt.options) {
        out.options = p.options.map((o) => ({ ...o, label: pt.options[o.value] ?? o.label }));
      }
      return out;
    }),
  };
}

