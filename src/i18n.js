// Internationalisation — DOM-free so the Web Worker can import it too.
// French stays the source of truth (registry + HTML defaults); this module adds
// English / Italian / Spanish overlays and the localisation helpers.
//
//   • t(key, vars)        — UI chrome strings (uses the current main-thread lang)
//   • unitLabel(units, l) — localised unit label
//   • localizeModel(m, l) — a registry model with label/description/tips/params
//                           swapped to language `l` (falls back to the French
//                           registry values when a translation is missing)
//   • am(lang)            — algorithm diagnostic messages (episode reasons,
//                           warnings) with locale-aware number formatting; the
//                           worker calls this with params.lang.
//
// The current language is a main-thread concern (selector + localStorage). The
// worker never reads it — algorithms receive the language through run params.

export const LANGS = [
  { code: "fr", label: "Français" },
  { code: "en", label: "English" },
  { code: "it", label: "Italiano" },
  { code: "es", label: "Español" },
];
export const DEFAULT_LANG = "fr";
const LOCALE = { fr: "fr-FR", en: "en-GB", it: "it-IT", es: "es-ES" };

// ---- current language (main thread only) -----------------------------------
let current = null;
const listeners = new Set();
export function getLang() {
  if (current) return current;
  try {
    const s = localStorage.getItem("predict2-lang");
    if (s && LANGS.some((l) => l.code === s)) current = s;
  } catch { /* worker / private mode: ignore */ }
  return current || DEFAULT_LANG;
}
export function setLang(code) {
  if (!LANGS.some((l) => l.code === code)) return;
  current = code;
  try { localStorage.setItem("predict2-lang", code); } catch { /* ignore */ }
  for (const fn of listeners) fn(code);
}
export function subscribeLang(fn) { listeners.add(fn); return () => listeners.delete(fn); }

// ---- number / duration / percent formatting --------------------------------
export function nf(lang, v, digits = 0) {
  if (!Number.isFinite(v)) return "—";
  return new Intl.NumberFormat(LOCALE[lang] || "fr-FR", {
    minimumFractionDigits: digits, maximumFractionDigits: digits,
  }).format(v);
}
const DUR = {
  fr: { d: "j", h: "h", min: "min" }, en: { d: "d", h: "h", min: "min" },
  it: { d: "g", h: "h", min: "min" }, es: { d: "d", h: "h", min: "min" },
};
export function fmtDur(lang, sec) {
  if (!Number.isFinite(sec) || sec <= 0) return "—";
  const u = DUR[lang] || DUR.fr;
  const h = sec / 3600;
  if (h >= 48) return `${nf(lang, Math.round(h / 24))} ${u.d}`;
  if (h >= 10) return `${nf(lang, Math.round(h))} ${u.h}`;
  if (h >= 1) return `${nf(lang, Math.round(h * 10) / 10, 1)} ${u.h}`;
  return `${nf(lang, Math.round(sec / 60))} ${u.min}`;
}
const pctSpace = (lang) => (lang === "en" ? "" : " ");
export const pct = (lang, v) => `${nf(lang, Math.round(v))}${pctSpace(lang)}%`;
export const signed = (lang, v, digits = 1) => (v >= 0 ? "+" : "−") + nf(lang, Math.abs(v), digits);

// ---- UI chrome dictionary ---------------------------------------------------
function interpolate(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (k in vars ? vars[k] : `{${k}}`));
}
export function t(key, vars, lang = getLang()) {
  const dict = UI[lang] || UI.fr;
  const s = (key in dict ? dict[key] : UI.fr[key]) ?? key;
  return interpolate(s, vars);
}

